package yyz.njupt.view.netService.UDPService;

public class Msg {
	

	private int recCode;
	private String recContend;
	private int fromUID;
	private int toUID;
	private String code;
	public int getRecCode() {
		return recCode;
	}
	public void setRecCode(int recCode) {
		this.recCode = recCode;
	}
	public String getRecContend() {
		return recContend;
	}
	public void setRecContend(String recContend) {
		this.recContend = recContend;
	}
	public int getFromUID() {
		return fromUID;
	}
	public void setFromUID(int fromUID) {
		this.fromUID = fromUID;
	}
	public int getToUID() {
		return toUID;
	}
	public void setToUID(int toUID) {
		this.toUID = toUID;
	}
	public String getCode() {
		return code;
	}
	public void setCode(String code) {
		this.code = code;
	}
	
	
	

}
